# создаем класс:
class Calculator:

# класс обладает методами:
    # умножения
   def multiply(self, x, y):
       return x * y

    # деления
   def division(self, x, y):
       return x / y

    # вычитания
   def subtraction(self, x, y):
       return x - y

    # сложения
   def adding(self, x, y):
       return x + y